function applyGlobalTranslations(lang) {
    const dict = _i18n[lang];
    if (!dict) return;

    // Process single strings
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            el.innerHTML = dict[key];
        }
    });

    // Process placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) el.placeholder = dict[key];
    });

    // Process messages with optional parameters
    document.querySelectorAll('[data-i18n-msg]').forEach(el => {
        const rawMsg = el.getAttribute('data-i18n-msg');
        if (!rawMsg) return;

        const parts = rawMsg.split('*');
        const key = parts[0];
        if (dict[key]) {
            let translated = dict[key];
            if (parts.length > 1) {
                translated += ' ' + parts.slice(1).join(' ');
            }
            el.innerHTML = translated;
        } else {
            // Fallback (keep original text)
        }
    });
}